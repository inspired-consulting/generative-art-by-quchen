data RGB = RGB !Double !Double !Double
    deriving (Eq, Ord, Show)

toColor :: RGB -> Color Double
toColor (RGB r g b) = rgb r g b

pickCyclic :: Vector a -> Int -> a
pickCyclic xs = \index ->
    let nColors = V.length xs
    in xs ! mod index nColors

